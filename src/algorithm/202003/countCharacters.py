

class Solution:
    def countCharacters(self, words: list, chars: str) -> int:
        count = 0
        for item in words:
            temp = 0
            temp_chars = chars
            for c in item:
                if c in temp_chars:
                    temp += 1
                    temp_chars.replace(c, '', 1)
            if temp == len(item):
                count += temp
        return count


